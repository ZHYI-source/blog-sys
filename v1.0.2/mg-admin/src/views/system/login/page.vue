import dayjs from 'dayjs'
import {mapActions} from 'vuex'
import localeMixin from '@/locales/mixin.js'
import util from "@/libs/util";
import {aes} from "@/libs/utils.crypto";
import {menuAside, menuHeader, setAsideMenu} from "@/menu";
import {getLogin, getLoginCaptcha} from "@/api/modules/sys.login.api";
import {dirRolesList} from "@/api/modules/sys.roles.api";
import {dirIPShare, recordVisitor} from "@/api/modules/sys.getIp.api";


export default {
  mixins: [
    localeMixin
  ],
  data() {
    return {
      timeInterval: null,
      time: dayjs().format('HH:mm:ss'),
      // 快速选择用户
      dialogVisible: false,
      users: [
        // {
        //   name: 'Admin',
        //   username: 'admin',
        //   password: 'admin'
        // },
        {
          name: 'Editor',
          username: 'editor',
          password: '123456'
        },
      ],
      // 表单
      formLogin: {
        username: '',
        password: '',
        code: ''
      },
      codeText: '',
      // 表单校验
      rules: {
        username: [
          {
            required: true,
            message: '请输入用户名',
            trigger: 'blur'
          }
        ],
        password: [
          {
            required: true,
            message: '请输入密码',
            trigger: 'blur'
          }
        ],
        code: [
          {
            required: true,
            message: '请输入验证码',
            trigger: 'blur'
          }
        ]
      }
    }
  },
  created() {
    this.getCodeSvg()
  },
  mounted() {
    this.timeInterval = setInterval(() => {
      this.refreshTime()
    }, 1000)
  },
  beforeDestroy() {
    clearInterval(this.timeInterval)
  },
  methods: {
    ...mapActions('d2admin/account', [
      'login'
    ]),
    refreshTime() {
      this.time = dayjs().format('HH:mm:ss')
    },
    /**
     * @description 获取验证码
     */
    getCodeSvg() {
      getLoginCaptcha().then(res => {
        this.formLogin.codeSvg = res.codeSvg || ''
        this.formLogin.key = res.key || ''
      })
    },

    /**
     * @description 接收选择一个用户快速登录的事件
     * @param {Object} user 用户信息
     */
    handleUserBtnClick(user) {
      this.formLogin.username = user.username
      this.formLogin.password = user.password
      this.dialogVisible = false
      // this.submit()
    },
    getIp(){
      dirIPShare().then(res=>{
        let ip = JSON.parse(res.slice(res.indexOf('{'),res.indexOf('}')+1)).cip
        //  去记录访客
        recordVisitor({ip,type:1}).then(res=>{
          console.log('record:ok')
        }).catch(err=>{
          console.log('错误',err)
        })
      }).catch(err=>{
        console.log('错误',err)
      })
    },
    /**
     * @description 提交表单
     */
    // 提交登录信息
    submit() {
      this.$refs.loginForm.validate((valid) => {
        if (valid) {
          getLogin({
            username: this.formLogin.username,
            password: aes.en(this.formLogin.password),
            code:this.formLogin.code,
          }).then(res => {
            util.cookies.set('uuid', res.userInfo.id)
            util.cookies.set('token', res.token)
            //设置用户名称
            this.$store.dispatch('d2admin/user/set', {name: res.userInfo.nickName}, {root: true})
            let roleId = res.userInfo.roleId
            //存储角色信息
            util.cookies.set('roleId', roleId)
            setAsideMenu(this)
            //记录访客
            this.getIp()
            // 重定向对象不存在则返回顶层路径
            this.$router.replace(this.$route.query.redirect || '/')
          }).catch(err => {
            this.getCodeSvg()
          })
        } else {
          this.getCodeSvg()
          // 登录表单校验失败
          this.$message.error('表单校验失败，请检查')
        }
      })
    }
  }
}
</script>

